// PackABI pack abi with abi content, pack name and arguments
func PackABI(content string, name string, args ...interface{}) ([]byte, error) {
	_abi, err := abi.JSON(strings.NewReader(content))
	if err != nil {
		return nil, err
	}
	return _abi.Pack(name, args...)
}
